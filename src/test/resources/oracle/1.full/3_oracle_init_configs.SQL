declare
	 rowCount int;
BEGIN
	select count(*) into rowCount from sys_config where ROWNUM <= 1;
	IF(rowCount > 0) THEN
		raise_application_error(-20000, 'sys_config表中已存在数据,请勿再执行全量初始化脚本,否则可能会对客户已经产生的配置数据进行覆盖!!');
	END IF;
END;
